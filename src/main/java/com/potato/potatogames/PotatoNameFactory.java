    public static String generatePotatoName() {
        Random r = new Random();

        boolean isMale = r.nextBoolean();
        String name;

        if (isMale)
        {
            name = MFname[ r.nextInt(MFname.length-1) ];
        } else {
            // female
            name = FFname[ r.nextInt(FFname.length-1) ];
        }

        name += " ";

        name += surnames[ r.nextInt(surnames.length-1) ];

        return name;

    }

